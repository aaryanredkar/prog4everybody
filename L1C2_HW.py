first_number = int(input("Enter First Number: "))
second_number = int(input("Enter Second Number: "))

sum = first_number + second_number
print ("Total of", first_number, "and", second_number,"is = ",sum)

x = first_number - second_number
print ("Difference of", first_number, "and", second_number,"is=",x)

multi= first_number * second_number
print ("Product of", first_number, "and", second_number,"is=",multi)

divide=first_number / second_number
print ("Quotient of", first_number, "and", second_number,"is=",divide)
