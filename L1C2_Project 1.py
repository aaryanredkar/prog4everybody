# Print String
str = "I am not crazy he said."
print(str)
print()

# To escape " or ' use \
str1 = "I am \"not crazy\" he said."
print(str1)
print()

# Use \n for new line.
str2 = "First line. \nSecond line."
print(str2)
print()

# Strings can be concatenated with the + operator.
str3 = "First part " +  "Second part"
print(str3)
print()

#Strings can be repeated with * operator.
str4 = "T" + 1000 * "oo"
print(str4)
print()
